#==============================================================================#
# ---- LIBRARIES ----
#==============================================================================#

library("drake")
library("here")
library("readr")

#==============================================================================#
# ---- FUNCTIONS ----
#==============================================================================#

source(here("R", "render.R"))

#==============================================================================#
# ---- PLAN ----
#==============================================================================#

plan <- drake_plan(
    configs = list(
        site = read_lines(here(file_in("pages/_site.yml"))),
        setup = read_lines(here(file_in("R/document_setup.R")))
    ),
    dataset = target(
        callr_render(
            here(knitr_in("pages/dataset.Rmd")),
            here(file_out("docs/dataset.html"))
        ),
        trigger = trigger(change = configs)
    )
)

#==============================================================================#
# ---- CONFIG ----
#==============================================================================#

drake_config(plan, verbose = 2)
